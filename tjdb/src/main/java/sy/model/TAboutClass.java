package sy.model;

import sy.frame.BaseData;

public class TAboutClass extends BaseData{

	private String id;
	private String name;//` VARCHAR(50) NULL COMMENT '约课人姓名',
	private String phone;//` VARCHAR(50) NULL COMMENT '约课人电话',
	private String grade;//` VARCHAR(50) NULL COMMENT '约课人年级',
	private String subject;//` VARCHAR(50) NULL COMMENT '约课人科目',
	private String type;//` VARCHAR(50) NOT NULL COMMENT '状态（0未读，1已读）',
	private String create_date;//
	
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}
